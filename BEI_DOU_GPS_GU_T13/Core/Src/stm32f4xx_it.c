/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
  //printf("进入中断!\n");
	uint8_t Res;
	
	if(__HAL_UART_GET_FLAG(&huart2,UART_FLAG_RXNE)!= RESET) 
		
	{
		assert_param(IS_USART_ALL_PERIPH(USART2));
		Res =(uint16_t )(USART2->DR & (uint16_t)0x01FF);//huart2.Instance->DR;//USART_ReceiveData(USART1);//(USART1->DR);	//读取接收到的数据 //读取接收寄存器
		
	//	printf("string RES and DR 寄存器 %c %c\n",Res,huart2.Instance->DR);
	
		if(Res == '$')
		{
		
		//	printf("RES %c\n",Res);
			point1 = 0;	
		}
		

		 USART_RX_BUF[point1++] = Res;
	
		//printf("BUFFER : %s \n",USART_RX_BUF);
		
		
		//printf("USART_RX_BUF len->>>>>>>>>>>> %d\n",sizeof(USART_RX_BUF));
		
		//printf("check  %c %c %c\n",USART_RX_BUF[0],USART_RX_BUF[4],USART_RX_BUF[5]);

		if(USART_RX_BUF[0] == '$' && USART_RX_BUF[4] == 'M' && USART_RX_BUF[5] == 'C')			//确定是否收到"GPRMC/GNRMC"这一帧数据
		{
			//	printf("GPRMC $\n");
			if(Res == '\n')									   
			{
					//printf("GPRMC ok$\n");
				memset(Save_Data.GPS_Buffer, 0, GPS_Buffer_Length);      //清空
				memcpy(Save_Data.GPS_Buffer, USART_RX_BUF, point1); 	//保存数据
				//	printf("%c\n",Save_Data.GPS_Buffer);
				Save_Data.isGetData = true;
				point1 = 0;
				memset(USART_RX_BUF, 0, USART_REC_LEN);      //清空				
			}	
					
		}
	//	printf("POINT %d USART_REC_LEN %d \n",point1,USART_REC_LEN);
	
		if(point1 >= USART_REC_LEN)
		{
			point1 = USART_REC_LEN;
		}	
			
	}
	  //HAL_UART_Receive_IT(&huart2,USART_RX_BUF,20);
	//	printf("leave2 %s\n",USART_RX_BUF);
	
	
		
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

